import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, '..');
const contentRoot = path.join(projectRoot, 'content', 'world-models');
const outputFile = path.join(
  projectRoot,
  'src',
  'features',
  'world-models',
  'content',
  'generated.ts'
);

const collections = [
  ['topics', null],
  ['papers', 'publication_date'],
  ['projects', 'release_date'],
  ['updates', 'date'],
  ['timeline', 'date'],
];

async function readCollection(directoryName, sortByDateField) {
  const directory = path.join(contentRoot, directoryName);
  const fileNames = (await fs.readdir(directory))
    .filter((fileName) => fileName.endsWith('.json'))
    .sort();

  const records = await Promise.all(
    fileNames.map(async (fileName) => {
      const filePath = path.join(directory, fileName);
      const raw = await fs.readFile(filePath, 'utf8');
      return JSON.parse(raw);
    })
  );

  if (!sortByDateField) {
    return records.sort((a, b) => String(a.slug).localeCompare(String(b.slug)));
  }

  return records.sort((a, b) =>
    String(b[sortByDateField]).localeCompare(String(a[sortByDateField]))
  );
}

const output = {};

for (const [directoryName, sortByDateField] of collections) {
  output[directoryName] = await readCollection(directoryName, sortByDateField);
}

const fileContents = `/* eslint-disable */\n// This file is auto-generated by scripts/generate-world-models-content.mjs.\n// Do not edit manually.\n\nexport const worldModelsContent = ${JSON.stringify(
  output,
  null,
  2
)} as const;\n`;

await fs.writeFile(outputFile, fileContents);
console.log(`Generated ${path.relative(projectRoot, outputFile)}`);
